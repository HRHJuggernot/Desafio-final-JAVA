package barberiapelofino;

public class Barbero extends Usuario{
    private boolean disponible;

    public Barbero() {
    }

    public Barbero(boolean disponible) {
        this.disponible = disponible;
    }

    public Barbero(boolean disponible, String nombre, String apellido, String correo, String estado, int id) {
        super(nombre, apellido, correo, estado, id);
        this.disponible = disponible;
    }

    public boolean isDisponible() {
        return disponible;
    }

    public void setDisponible(boolean disponible) {
        this.disponible = disponible;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getApellido() {
        return apellido;
    }

    public void setApellido(String apellido) {
        this.apellido = apellido;
    }

    public String getCorreo() {
        return correo;
    }

    public void setCorreo(String correo) {
        this.correo = correo;
    }

    public String getEstado() {
        return estado;
    }

    public void setEstado(String estado) {
        this.estado = estado;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }
    
}
